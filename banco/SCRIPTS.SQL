--CRIAÇÃO BD
CREATE SCHEMA `memory_game` DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci ;


--CRIAÇÃO TABELA CADASTROS
CREATE TABLE `memory_game`.`cadastros` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `NOME` VARCHAR(200) NOT NULL,
  `DATANASC` DATE NOT NULL,
  `CPF` VARCHAR(45) NOT NULL,
  `TELEFONE` VARCHAR(45) NOT NULL,
  `EMAIL` VARCHAR(45) NOT NULL,
  `USERNAME` VARCHAR(200) NOT NULL,
  `SENHA` VARCHAR(200) NOT NULL,
  PRIMARY KEY (`ID`))
ENGINE = InnoDB;

--CRIAÇÃO TABELA HISTORICO
CREATE TABLE `memory_game`.`historico` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `USUARIO` VARCHAR(45) NOT NULL,
  `DIMENSAO` VARCHAR(45) NOT NULL,
  `MODALIDADE` VARCHAR(45) NOT NULL,
  `TEMPO` INT NOT NULL,
  `RESULTADO` VARCHAR(45) NOT NULL,
  `DATA` DATETIME NOT NULL,
  `JOGADAS` INT NOT NULL,
  PRIMARY KEY (`ID`));
